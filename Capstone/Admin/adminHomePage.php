<h4>A Few Notes to Keep In Mind:</h4>

<ul>
    <li>Instructors and the Admin can create new Student accounts, but only the Admin can create new Instructor accounts</li>
    <li>Deletion of accounts/profiles/posts is irreversable.</li>
    <li>Only the Admin can delete Instructor profiles</li>
    <li>Instructors can delete Student profiles</li>
    <li>Instructors can only delete material they've posted/uploaded to the site</li>
</ul>